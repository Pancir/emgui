use crate::core::control::focus::FocusManager;
use std::cell::RefCell;
use std::rc::Rc;

////////////////////////////////////////////////////////////////////////////////////////////////////

struct InnerRuntime {
   focus: FocusManager,
}

////////////////////////////////////////////////////////////////////////////////////////////////////

#[derive(Clone)]
pub struct Runtime {
   inner: RefCell<Rc<InnerRuntime>>,
}

impl Runtime {
   pub fn new() -> Self {
      Self { inner: RefCell::new(Rc::new(InnerRuntime { focus: FocusManager::new() })) }
   }
}

