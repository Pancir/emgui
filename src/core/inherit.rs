use std::any::Any;

////////////////////////////////////////////////////////////////////////////////////////////////////

pub trait Inherit: Any + 'static {
   fn as_any(&self) -> &dyn Any;
   fn as_any_mut(&mut self) -> &mut dyn Any;
}

impl Inherit for () {
   fn as_any(&self) -> &dyn Any {
      self
   }

   fn as_any_mut(&mut self) -> &mut dyn Any {
      self
   }
}

